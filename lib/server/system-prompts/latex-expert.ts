    '- biblatex: \\autocite{}, \\textcite{}, \\printbibliography, runs biber. Preferred for new projects.',
    '- Common issue: "undefined citation" -- usually means biber/bibtex was not run after latex.',
    '',
    '--- TikZ ---',
