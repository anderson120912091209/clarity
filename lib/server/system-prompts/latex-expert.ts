    '- Common patterns: \\foreach \\x in {1,...,5} { ... }, relative positioning with "right=of nodeA".',
    '',
    '--- Beamer ---',
    '- Structure: \\begin{frame}{Title} ... \\end{frame}.',
    '- Themes: \\usetheme{Madrid}, \\usecolortheme{whale}, \\usefonttheme{serif}.',
